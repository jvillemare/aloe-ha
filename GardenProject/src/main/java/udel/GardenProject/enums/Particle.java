package udel.GardenProject.enums;

/**
 * Types of items that can be scattered on the window view. Used in Season View 
 * window.
 * 
 * @author Team 0
 */
public enum Particle {
	
	/**
	 * Specify that snow particles should appear in the SeasonView.
	 */
	SNOW,
	
	/**
	 * Specify that leaves particles should appear in the SeasonView.
	 */
	LEAVES;
	
}
